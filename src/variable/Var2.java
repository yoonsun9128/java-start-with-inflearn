package variable;

public class Var2 {
    public static void main(String[] args) {
        int a; //변수 선언 => 변환이 되는 값
        a = 20; //변수 초기화

        System.out.println(a);
        System.out.println(a);
        System.out.println(a);

        a = 40; //변수 값 변경
        System.out.println(a);
    }
}
